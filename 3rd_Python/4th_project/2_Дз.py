# Создаю переменную для хранения всех продуктов на складе. Название и кол-во продуктов
# Имитация базы данных: all_products = {"Картофель": 10, "Морковь": 30}
all_products = {"Картофель": 10, "Морковь": 30}

# Цикл для симуляции повторений
while True:
    # Создаю меню предоставления выбора для пользователя
    menu = input("Выберите действие:\n"
                 "1 - Добавить товар\n"
                 "2 - Посмотреть склад\n"
                 "3 - Удалить товар\n"
                 "4 - Выход\n")

    # Реализация логики для добавления товара
    if menu == "1":
        product_name = input("Введите название товара: ")
        product_count = int(input("Введите количество товара: "))  # Преобразуем ввод в число
        if product_name in all_products:
            all_products[product_name] += product_count
            print(f"Количество товара '{product_name}' обновлено. Теперь {product_name}: {all_products[product_name]} кг.")
        else:
            all_products[product_name] = product_count
            print(f"Продукт '{product_name}' добавлен в склад в количестве {product_count} кг.")
    # Реализация логики для просмотра склада (БД.)
    elif menu == "2":
        # if <переменная>: проверяет, является ли переменная истинной (True).
        # Словарь считается False, если он пуст ({}), и True, если в нем есть элементы.
        if all_products:
            print("Склад:")
            number = 1 # Создаём переменную для хранения нумераций элементов словаря.
            for key in all_products: # Перебираем все ключи в словаре
                value = all_products[key] # Получаем значение по ключу
                print(f"{number}. {key} - {value} кг.")
                number += 1 # Увеличиваем номер для следующего товара на один.
        else:
            print("Склад пуст.")
    # Реализация логики для удаления товара из БД.
    elif menu == "3":
        product_name = input("Введите название товара для удаления: ")
        if product_name in all_products:
            product_count = int(input("Введите количество для удаления: "))
            if product_count >= all_products[product_name]:
                del all_products[product_name]
                print(f"Товар '{product_name}' полностью удалён со склада.")
            else:
                all_products[product_name] -= product_count
                print(f"Товар '{product_name}' уменьшен на {product_count} кг. Остаток на складе: {all_products[product_name]} кг.")
                print("Если хотите удалить товар полностью, введите количество, равное или больше текущего остатка.")
        else:
            print(f"Товара '{product_name}' нет на складе.")
    # Выход из программы
    elif menu == "4":
        print("Выход из программы.")
        break
    # Обработка ошибки
    else:
        print("Неверный выбор! Выберите из списка от 1 до 4.")

