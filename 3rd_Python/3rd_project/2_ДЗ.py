contacts = {}

# –°–æ–∑–¥–∞–µ–º –ª–æ–≥–∏–∫—É –¥–ª—è –¥–æ–±–∞–≤–ª–µ–Ω–∏—è –∫–æ–Ω—Ç–∞–∫—Ç–∞ —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º —Ñ—É–Ω–∫—Ü–∏–∏
def add_contact(name, phone):
    if name in contacts:
        print(f"–ö–æ–Ω—Ç–∞–∫—Ç —Å –∏–º–µ–Ω–µ–º {name} —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç!")
    else:
        contacts[name] = phone
        print(f"–ö–æ–Ω—Ç–∞–∫—Ç {name} –¥–æ–±–∞–≤–ª–µ–Ω(–∞).")

# –°–æ–∑–¥–∞–µ–º –ª–æ–≥–∏–∫—É –¥–ª—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –∫–æ–Ω—Ç–∞–∫—Ç–∞ —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º —Ñ—É–Ω–∫—Ü–∏–∏
def update_contact(name, new_phone_num):
    if name in contacts:
        contacts[name] = new_phone_num
        print(f"–ö–æ–Ω—Ç–∞–∫—Ç {name} –æ–±–Ω–æ–≤–ª–µ–Ω(–∞).")
    else:
        print(f"–ö–æ–Ω—Ç–∞–∫—Ç {name} –Ω–µ –Ω–∞–π–¥–µ–Ω.")

# –°–æ–∑–¥–∞–µ–º –ª–æ–≥–∏–∫—É –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è –∫–æ–Ω—Ç–∞–∫—Ç–∞ —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º —Ñ—É–Ω–∫—Ü–∏–∏
def delete_contact(name):
    if name in contacts:
        del contacts[name]
        print(f"–ö–æ–Ω—Ç–∞–∫—Ç {name} —É–¥–∞–ª–µ–Ω(–∞).")
    else:
        print(f"–ö–æ–Ω—Ç–∞–∫—Ç {name} –Ω–µ –Ω–∞–π–¥–µ–Ω.")

# –°–æ–∑–¥–∞–µ–º –ª–æ–≥–∏–∫—É –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –∫–æ–Ω—Ç–∞–∫—Ç–∞(–æ–≤) —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º —Ñ—É–Ω–∫—Ü–∏–∏
def display_contacts():
    print("\n –°–ø–∏—Å–æ–∫ –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤: " if contacts else "–°–ø–∏—Å–æ–∫ –∫–æ–Ω—Ç–∞–∫—Ç–æ–≤ –ø—É—Å—Ç.")
    for name, phone in contacts.items():
        print(f"üìû {name}: {phone}")

